    private void BntCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntCadastroActionPerformed
        // TODO add your handling code here:
        Usuario usuario = new Usuario();
        usuario.setNome_usuario(jTextField3.getText());
        usuario.setSenha_usuario(jPasswordField1.getText());
        String sql = "select * from usuario where nome_usuario = ?";
        ConnectionFactory factory = new ConnectionFactory();
        try (Connection con = factory.obtemConexao()) {
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, usuario.getNome_usuario());
            ResultSet rs = ps.executeQuery();
            if(jTextField3.getText().equalsIgnoreCase("") || jPasswordField1.getText().equalsIgnoreCase("")){
                JOptionPane.showMessageDialog(null,"NÃ£o pode cadastrar usuario e senha vazio");
            }
            else if(rs.next()){
                JOptionPane.showMessageDialog(null,"Erro ao Cadastrar Usuario");
                jPasswordField1.setText("");
                jTextField3.setText("");
            }
            else{
                usuario.cadastrar();
                dispose();
            }
        } catch(SQLException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_BntCadastroActionPerformed

